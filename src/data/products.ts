export function getProductBySlug(slug: string) {
  return products.find((p) => p.slug === slug);
}

export function getProductById(id: string) {
  return products.find((p) => p.id === id);
}

export function getProductBySku(sku: string) {
  return products.find((p) => p.sku === sku);
}

export function getFeaturedProducts() {
  return products.filter((p) => p.featured);
}

export function getProductsByZone(zoneId: string) {
  return products.filter((p) => p.functionalZones.includes(zoneId as Product["functionalZones"][number]));
}

export function getProductsByRoom(roomLabel: string) {
  const normalized = roomLabel.toLowerCase();
  return products.filter((p) =>
    p.rooms.some((r) => r.toLowerCase().includes(normalized) || normalized.includes(r.toLowerCase())),
  );
}

export function getProductsByCategory(categorySlug: string) {
  const map: Record<string, (p: Product) => boolean> = {
    "desks-workstations": (p) =>
      ["Desks", "Executive Desks", "Corner Workstations", "Compact Desks"].includes(p.category),
    "writing-desks": (p) => p.subcategory === "Writing Desks" || p.sku === "BDS-DSK-001",
    "executive-desks": (p) => p.category === "Executive Desks",
    "corner-workstations": (p) => p.category === "Corner Workstations",
    "meeting-tables": (p) => p.category === "Meeting Tables" || p.category === "Conference Tables",
    "training-tables": (p) => p.category === "Training Tables",
    "task-chairs": (p) => p.category === "Task Chairs" || p.category === "Swivel Chairs",
    "guest-chairs": (p) => p.category === "Guest Chairs",
    "lounge-seating": (p) => p.category === "Lounge Seating",
    loveseats: (p) => p.category === "Loveseats",
    "benches-ottomans": (p) => p.category === "Benches" || p.category === "Ottomans",
    storage: (p) =>
      ["Storage", "Credenzas", "Bookcases", "Open Storage"].includes(p.category),
    credenzas: (p) => p.category === "Credenzas",
    bookcases: (p) => p.category === "Bookcases",
    "home-office": (p) => p.rooms.some((r) => r.toLowerCase().includes("home office")),
  };
  const predicate = map[categorySlug];
  return predicate ? products.filter(predicate) : [];
}

